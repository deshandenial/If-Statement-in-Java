package if_statement;

public class If_Statement
{
	// initialize the data type variable
	static int x = 10;
	
	public static void main(String[] args) 
	{
		// boolean expression condition 
		if(x == 10)
		{
			// if whether is satisfy the boolean condition execute the block
			System.out.println("print the x value:"+x);
		}
		
		// if whether it is fails the boolean condition does not execute the block
		
	}
	
	
	
	

}
