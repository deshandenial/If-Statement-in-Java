package if_statement;

public class If_Else_Statement
{
  public static void main(String[] args) 
  {
	  // initiliaze the data type variable
	int x=10;
	
	// declare the boolean expression in a if block
	if(x==20)
	{
		// if whether is satisfy the boolean condition execute the block
		System.out.println("print the x value:"+x);
	}else
	{
		// if whether it is fails the boolean condition does not execute the block
		System.out.println("print the else default statement");
	}
	
	
}
}
